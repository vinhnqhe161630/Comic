package model;

/**
 *
 * @author HP
 */
public class Account {

    private int ac_id;
    private String user, password,email;
    private String fname,lname;
    private int status,role;
    private int gender;

    public Account() {
    }

    public Account(int ac_id, String user, String password, String email, String fname, String lname, int status, int role,int gender) {
        this.ac_id = ac_id;
        this.user = user;
        this.password = password;
        this.email = email;
        this.fname = fname;
        this.lname = lname;
        this.status = status;
        this.role = role;
        this.gender = gender;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public int getAc_id() {
        return ac_id;
    }

    public void setAc_id(int ac_id) {
        this.ac_id = ac_id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

  

}
